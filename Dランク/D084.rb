month = gets.to_i

if month == 1
  puts "January"
elsif month == 2
  puts "February"
elsif month == 3
  puts "March"
elsif month == 4
  puts "April"
elsif month == 5
  puts "May"
elsif month == 6
  puts "June"
elsif month == 7
  puts "July"
elsif month == 8
  puts "August"
elsif month == 9
  puts "September"
elsif month == 10
  puts "October"
elsif month == 11
  puts "November"
else
  puts "December"
end